"""Functions for parsing various file formats."""
from typing import List, Sequence, Tuple


def parse_fasta(fasta_string: str) -> Tuple[Sequence[str], Sequence[str]]:
    """Parses FASTA string and returns list of strings with amino-acid sequences.

    Arguments:
        fasta_string: The string contents of a FASTA file.

    Returns:
        A tuple of two lists:
        * A list of sequences.
        * A list of sequence descriptions taken from the comment lines. In the
        same order as the sequences.
    """
    sequences = []
    descriptions = []
    index = -1
    for line in fasta_string.splitlines():
        line = line.strip()
        if line.startswith(">"):
            index += 1
            descriptions.append(line[1:])  # Remove the '>' at the beginning.
            sequences.append("")
            continue
        elif not line:
            continue  # Skip blank lines.
        sequences[index] += line

    return sequences, descriptions


def format_fasta_string(all_sequences: List[str], all_descriptions: List[str]) -> str:
    """Formats a list of sequences along with their descriptions into a fasta string."""
    assert len(all_sequences) == len(all_descriptions)
    return "\n".join(
        [
            f">{description}\n{sequence}"
            for (sequence, description) in zip(all_sequences, all_descriptions)
        ]
    )
